/* Print the pattern:
 * "*"
 * "**"
 * "***"
 * "****"
 */
public class Task1 {
    // Define the main method, the entry point of the program
    public static void main(String[] args) {
        // Print a single asterisk "*"
        System.out.println("*");
        
        // Print two asterisks "**"
        System.out.println("**");
        
        // Print three asterisks "***"
        System.out.println("***");
        
        // Print four asterisks "****"
        System.out.println("****");
    }
}
